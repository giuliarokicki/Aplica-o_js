//Faça um programa que mostre os números impares em um intervalo de 0 a 100.
let numero = 1; // Começa com o primeiro número ímpar

while (numero <= 100) { // Enquanto o número for menor ou igual a 100
    console.log(numero); // Mostra o número no console
    numero += 2; // Soma 2 para ir para o próximo ímpar
}